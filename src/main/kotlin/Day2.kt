fun needToDo(letter: String): ((Int)->Int) {
    return when (letter) {

        "X" -> ::lose // lose
        "Y" -> ::draw // draw
        "Z" -> ::win // win

        else -> error("")
    }
}

fun needToDoScore(letter: String): Int {
    return when (letter) {

        "X" -> 0 // lose
        "Y" -> 3 // draw
        "Z" -> 6 // win

        else -> error("")
    }
}

fun lose(shape: Int): Int {
    return when (shape) {
        1 -> 3
        2 -> 1
        3 -> 2
        else -> error("")
    }
}
fun win(shape: Int): Int {
    return when (shape) {
        1 -> 2
        2 -> 3
        3 -> 1
        else -> error("")
    }
}
fun draw(shape: Int): Int {
    return shape
}


fun scoreForShape(shape: String): Int =
    when (shape) {
        "A" -> 1 // rock
        "B" -> 2 // paper
        "C" -> 3 // scissors

        "X" -> 1 // rock
        "Y" -> 2 // paper
        "Z" -> 3 // scissors

        else -> Int.MIN_VALUE
    }

