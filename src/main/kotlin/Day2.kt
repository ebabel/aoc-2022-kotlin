        scoreForShape(opponent) == scoreForShape(shape) -> 3
        scoreForShape(opponent) == 1 && scoreForShape(shape) == 2 -> 6
        scoreForShape(opponent) == 2 && scoreForShape(shape) == 1 -> 0
        scoreForShape(opponent) == 1 && scoreForShape(shape) == 3 -> 0
        scoreForShape(opponent) == 2 && scoreForShape(shape) == 3 -> 6
        scoreForShape(opponent) == 3 && scoreForShape(shape) == 2 -> 0
        scoreForShape(opponent) == 3 && scoreForShape(shape) == 1 -> 6
        else -> Int.MIN_VALUE
    }


