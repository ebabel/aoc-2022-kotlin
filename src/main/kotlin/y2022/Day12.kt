data class Node(val location: Point, val elevation: Int) {
    fun getNeighbors(map: List<List<Node>>): List<Node> {
        return buildList {
            if (location.y < map.size - 1) add(map[location.y + 1][location.x])
            if (location.y > 0) add(map[location.y - 1][location.x])
            if (location.x < map[0].size - 1) add(map[location.y][location.x + 1])
            if (location.x > 0) add(map[location.y][location.x - 1])
        }
    }

}

private data class NodePathMap(val node: Node, val path: List<Node>)
class Day12(private val input: String) {

    fun part1(): String {
        val map = parseMap()

//        debugPath(foundPath)
        return bfs(
            start = map.flatten().first { it.elevation == -1 },
            end = map.flatten().first { it.elevation == 26 },
            map = map,
            validity = {neighbor, current -> neighbor.elevation <= current.elevation + 1}
        ).size.toString()
    }

    fun part2(): String {
        val map = parseMap()

//        debugPath(foundPath)
        return bfs(
            start = map.flatten().first { it.elevation == 26 },
            end = map.flatten().first { it.elevation == -1 },
            map = map,
            validity = {neighbor, current -> current.elevation <= neighbor.elevation + 1}
        ).also { debugPath(it) }.size.toString()
    }

    private fun parseMap() = input.lines().mapIndexed { y, s ->
        s.mapIndexed { x, letter ->
            val elevation = when (letter) {
                'S' -> -1
                'E' -> 26
                else -> letter - 'a'
            }
            Node(Point(x, y), elevation)
        }
    }
}

fun bfs(start: Node, end: Node, map: List<List<Node>>, validity:(Node, Node) -> Boolean): List<Node> {
    val nodeQueue = ArrayDeque<NodePathMap>()
    nodeQueue.add(NodePathMap(start, emptyList()))
    val visited = mutableListOf<Node>()

    while (nodeQueue.isNotEmpty()) {
        val current = nodeQueue.removeFirst()
        current.node.getNeighbors(map)
            .filter { it !in visited }
            .filter { validity(it, current.node) }
            .forEach { node ->
                if (node == end) {
                    return current.path.plus(node)
                }
                nodeQueue.add(NodePathMap(node, current.path.plus(node)))
                visited.add(node)
            }
    }
    error("Bfs not found")
}

private fun debugPath(path: List<Node>) {
    buildString {
        (0..(path.maxOf { it.location.y })).forEach { y ->
            (0..(path.maxOf { it.location.x })).forEach { x ->
                val index = path.indexOfFirst { it.location.x == x && it.location.y == y }
                append(
                    if (index == -1) {
                        " "
                    } else {
                        "."
                    }
                )
            }
            appendLine()
        }
    }.also { println(it) }
}

